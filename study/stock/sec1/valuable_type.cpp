#include <iostream>
// using namespace std;

int main() {
  // 一年の秒数
  int seconds = 365 * 24 * 60 * 60;

  // 以下のコメント/* */を消して追記する
  std::cout << "1年:" << seconds * 1 << std::endl;
  std::cout << "2年:" << seconds * 2 << std::endl;
  std::cout << "5年:" << seconds * 5 << std::endl;
  std::cout << "10年:" << seconds * 10 << std::endl;
}
